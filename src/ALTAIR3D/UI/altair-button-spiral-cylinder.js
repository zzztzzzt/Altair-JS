import * as THREE from 'three';
import { GLTFLoader } from 'three/addons/loaders/GLTFLoader.js';
import spiralCylinder from '@models/spiral-cylinder.glb';

export class ButtonSpiralCylinder {
    constructor(color = 0, useEnvMap = false) {
        // 1. Variables
        this.objectType = 'button';

        this.useEnvMap = useEnvMap;

        this.color = color;
        let colorTypeOne = {};
        let colorTypeTwo = {};
        let colorCustom = {};
        this.colorTypeList = [colorTypeOne, colorTypeTwo, colorCustom];

        this.rotationSpeed = 0.15;

        this.glowSpheresData = [];

        // hover / click status
        this.hoverTarget = 0; // 0 = not hover，1 = hover
        this.hoverBlend = 0; // This value follow hoverTarget（ each frame is easing, not an instantaneous switch ）
        this.hoverScaleBoost = 0.06; // Overall zoom level when hovering
        this.hoverRotationMultiplier = 1.8; // Rotation speed ratio while hovering
        this.hoverRimIntensity = 0.2; // The intensity of the faint light that appears on the surface of the model when hovering
        this.rimGlowColor = 0xa8d8ff; // Model hovering in the dim light color

        this.clickPulse = 0; // Clicking it briefly pushes the value to 1, then decays back to 0
        this.clickShockwaves = []; // Click on the resulting diffused halo; it will automatically disappear when the animation ends
        this.lastElapsed = 0; // Record the latest elapsed

        // 2. Meshes
        let glbModel = null;
        this.mainMesh = glbModel;
        this.materials = [];
        this.baseScale = new THREE.Vector3(1, 1, 1); // Real scaling without hover / click effects

        // 3. Lights

        // 4. Event Listeners
        this.whenMouseOver = () => {
            this.hoverTarget = 1;
            this.customizeWhenMouseOver();
        };

        this.notMouseOver = () => {
            this.hoverTarget = 0;
            this.customizeNotMouseOver();
        };

        this.whenClick = () => {
            // The scaling impact, model glow, and pulsating glowing spheres are all driven by clickPulse
            // the details are in animateFunc

            this.clickPulse = 1;
            this.spawnClickShockwave(this.lastElapsed);
            this.customizeWhenClick();
        };

        this.whenMouseMove = (x, y) => {};

        this.customizeWhenMouseOver = () => {};

        this.customizeNotMouseOver = () => {};

        this.customizeWhenClick = () => {};

        // 5. Animation
        this.animateFunc = (delta, elapsed, timestamp) => {
            this.lastElapsed = elapsed;

            // hoverBlend smoothly follows hoverTarget
            this.hoverBlend += (this.hoverTarget - this.hoverBlend) * Math.min(1, delta * 6);
            // ClickPulse initially spikes to 1, then naturally decays back to 0
            this.clickPulse += (0 - this.clickPulse) * Math.min(1, delta * 5);

            if (this.mainMesh) {
                const speedMultiplier = 1 + (this.hoverRotationMultiplier - 1) * this.hoverBlend;
                this.mainMesh.rotation.y += delta * this.rotationSpeed * speedMultiplier;

                // Slightly enlarge while hovering, then add a brief zoom-in/zoom-out effect upon clicking
                const scaleMultiplier = 1 + this.hoverScaleBoost * this.hoverBlend + this.clickPulse * 0.12;
                this.mainMesh.scale.set(
                    this.baseScale.x * scaleMultiplier,
                    this.baseScale.y * scaleMultiplier,
                    this.baseScale.z * scaleMultiplier
                );
            }

            const rimIntensity = this.hoverRimIntensity * this.hoverBlend + this.clickPulse * 1.5;
            this.materials.forEach((material) => {
                material.emissiveIntensity = rimIntensity;
            });

            // Each luminous sphere moves up and down and then drifts in a small circular motion according to its own phase/velocity
            const sphereGlowBoost = this.hoverBlend * 0.6 + this.clickPulse * 2.5;
            const sphereScaleBoost = 1 + this.hoverBlend * 0.25 + this.clickPulse * 0.5;
            this.glowSpheresData.forEach((data) => {
                const { mesh, basePosition, floatSpeed, floatRange, orbitSpeed, orbitRadius, phase, baseEmissiveIntensity } = data;
                mesh.position.y = basePosition.y + Math.sin(elapsed * floatSpeed + phase) * floatRange;
                mesh.position.x = basePosition.x + Math.cos(elapsed * orbitSpeed + phase) * orbitRadius;
                mesh.position.z = basePosition.z + Math.sin(elapsed * orbitSpeed * 0.8 + phase) * orbitRadius;
                mesh.material.emissiveIntensity = baseEmissiveIntensity + sphereGlowBoost;
                mesh.scale.setScalar(sphereScaleBoost);
            });

            // Update and clear the diffusion halo generated by clicks
            this.updateClickShockwaves(elapsed);
        };

        // 6. Functions
    }

    async loadModelAsync(GlbPath = spiralCylinder) {
        const loader = new GLTFLoader();
        try {
            const gltf = await this.loadModel(loader, GlbPath);
            this.mainMesh = gltf.scene;

            this.applyMaterial(this.mainMesh);

            this.createGlowSpheres(this.mainMesh, 6);

            this.isModelLoaded = true;
        } catch (error) {
            console.error('Loading GLTF error', error);
        }
    }

    loadModel(loader, GlbPath) {
        return new Promise((resolve, reject) => {
            loader.load(
                GlbPath,
                (gltf) => resolve(gltf),
                (xhr) => {
                    // console.log('GLB model ' + (xhr.loaded / xhr.total * 100) + '% loaded');
                },
                (error) => reject(error)
            );
        });
    }

    applyMaterial(root) {
        this.materials = [];
        const baseColor = this.color ? this.color : 0xb5b5b5;

        root.traverse((child) => {
            if (child.isMesh) {
                const material = new THREE.MeshPhysicalMaterial({
                    color: baseColor,
                    metalness: 0.55,
                    roughness: 0.3,
                    clearcoat: 0.4,
                    clearcoatRoughness: 0.25,
                    envMapIntensity: this.useEnvMap ? 1.3 : 1,
                    side: THREE.DoubleSide,
                    emissive: new THREE.Color(this.rimGlowColor),
                    emissiveIntensity: 0,
                });
                child.material = material;
                child.castShadow = true;
                child.receiveShadow = true;
                this.materials.push(material);
            }
        });
    }

    createGlowSpheres(root, count = 6) {
        this.glowSpheresData = [];

        const box = new THREE.Box3().setFromObject(root);
        const size = new THREE.Vector3();
        box.getSize(size);
        const center = new THREE.Vector3();
        box.getCenter(center);
        root.worldToLocal(center);

        const colorStart = new THREE.Color(0xc9a8ff);
        const colorEnd = new THREE.Color(0xa8d8ff);
        const maxDim = Math.max(size.x, size.y, size.z, 0.001);

        for (let i = 0; i < count; i++) {
            const t = count > 1 ? i / (count - 1) : 0;
            const color = colorStart.clone().lerp(colorEnd, t);
            const radius = THREE.MathUtils.randFloat(0.015, 0.03) * maxDim;

            const sphere = this.createGlowSphere(color, radius);

            const basePosition = new THREE.Vector3(
                center.x + THREE.MathUtils.randFloatSpread(size.x * 0.35),
                center.y + THREE.MathUtils.randFloatSpread(size.y * 0.35),
                center.z + THREE.MathUtils.randFloatSpread(size.z * 0.35)
            );
            sphere.position.copy(basePosition);
            root.add(sphere);

            this.glowSpheresData.push({
                mesh: sphere,
                basePosition: basePosition,
                baseEmissiveIntensity: sphere.material.emissiveIntensity,
                floatSpeed: THREE.MathUtils.randFloat(0.3, 0.7),
                floatRange: THREE.MathUtils.randFloat(0.02, 0.05) * maxDim,
                orbitSpeed: THREE.MathUtils.randFloat(0.15, 0.35),
                orbitRadius: THREE.MathUtils.randFloat(0.015, 0.035) * maxDim,
                phase: Math.random() * Math.PI * 2,
            });
        }
    }

    createGlowSphere(color, radius) {
        const geometry = new THREE.SphereGeometry(radius, 16, 16);
        const material = new THREE.MeshStandardMaterial({
            color: color,
            emissive: color,
            emissiveIntensity: 3.8,
            roughness: 0.2,
            metalness: 0,
            transparent: true,
            opacity: 0.95,
        });
        const sphere = new THREE.Mesh(geometry, material);

        const glowTexture = this.getGlowTexture(color);

        const coreGlow = new THREE.Sprite(new THREE.SpriteMaterial({
            map: glowTexture,
            color: 0xffffff,
            transparent: true,
            blending: THREE.AdditiveBlending,
            depthWrite: false,
            opacity: 1,
        }));
        const coreScale = radius * 4;
        coreGlow.scale.set(coreScale, coreScale, 1);
        sphere.add(coreGlow);

        const outerGlow = new THREE.Sprite(new THREE.SpriteMaterial({
            map: glowTexture,
            color: color,
            transparent: true,
            blending: THREE.AdditiveBlending,
            depthWrite: false,
            opacity: 1,
        }));
        const outerScale = radius * 9; // The larger the number, the larger and softer the halo
        outerGlow.scale.set(outerScale, outerScale, 1);
        sphere.add(outerGlow);

        return sphere;
    }

    // Textures of the same color will share the same cache to avoid duplicate textures
    getGlowTexture(color) {
        const c = new THREE.Color(color);
        const key = c.getHexString();
        this.glowTextureCache = this.glowTextureCache || {};
        if (this.glowTextureCache[key]) return this.glowTextureCache[key];

        const size = 128;
        const canvas = document.createElement('canvas');
        canvas.width = size;
        canvas.height = size;
        const ctx = canvas.getContext('2d');

        const r = Math.round(c.r * 255);
        const g = Math.round(c.g * 255);
        const b = Math.round(c.b * 255);

        const gradient = ctx.createRadialGradient(size / 2, size / 2, 0, size / 2, size / 2, size / 2);
        gradient.addColorStop(0, `rgba(${r}, ${g}, ${b}, 1)`);
        gradient.addColorStop(0.25, `rgba(${r}, ${g}, ${b}, 0.85)`);
        gradient.addColorStop(0.55, `rgba(${r}, ${g}, ${b}, 0.45)`);
        gradient.addColorStop(1, `rgba(${r}, ${g}, ${b}, 0)`);

        ctx.fillStyle = gradient;
        ctx.fillRect(0, 0, size, size);

        const texture = new THREE.CanvasTexture(canvas);
        texture.needsUpdate = true;
        this.glowTextureCache[key] = texture;
        return texture;
    }

    getRingTexture() {
        if (this.ringTextureCache) return this.ringTextureCache;

        const size = 128;
        const canvas = document.createElement('canvas');
        canvas.width = size;
        canvas.height = size;
        const ctx = canvas.getContext('2d');
        const cx = size / 2;
        const cy = size / 2;

        const gradient = ctx.createRadialGradient(cx, cy, size * 0.28, cx, cy, size * 0.5);
        gradient.addColorStop(0, 'rgba(255, 255, 255, 0)');
        gradient.addColorStop(0.55, 'rgba(255, 255, 255, 0.9)');
        gradient.addColorStop(0.68, 'rgba(255, 255, 255, 0.9)');
        gradient.addColorStop(1, 'rgba(255, 255, 255, 0)');

        ctx.fillStyle = gradient;
        ctx.fillRect(0, 0, size, size);

        const texture = new THREE.CanvasTexture(canvas);
        texture.needsUpdate = true;
        this.ringTextureCache = texture;
        return texture;
    }

    spawnClickShockwave(elapsed) {
        if (!this.mainMesh) return;

        const box = new THREE.Box3().setFromObject(this.mainMesh);
        const size = new THREE.Vector3();
        box.getSize(size);
        const baseRadius = Math.max(size.x, size.y, size.z, 0.001) * 0.4;
        const color = new THREE.Color(0xc2b3ff);

        // Key flash : Rapid zoom-in, rapid fade-out
        const flashMaterial = new THREE.SpriteMaterial({
            map: this.getGlowTexture(color),
            color: 0xffffff,
            transparent: true,
            blending: THREE.AdditiveBlending,
            depthWrite: false,
            opacity: 1,
        });
        const flashSprite = new THREE.Sprite(flashMaterial);
        flashSprite.scale.setScalar(baseRadius * 0.6);
        this.mainMesh.add(flashSprite);
        this.clickShockwaves.push({
            mesh: flashSprite,
            material: flashMaterial,
            startTime: elapsed,
            duration: 0.25,
            startScale: baseRadius * 0.6,
            endScale: baseRadius * 1.1,
            startOpacity: 1,
            endOpacity: 0,
        });

        // Diffusion halo : Slowly spreads outwards and gradually fades out
        const ringMaterial = new THREE.SpriteMaterial({
            map: this.getRingTexture(),
            color: color,
            transparent: true,
            blending: THREE.AdditiveBlending,
            depthWrite: false,
            opacity: 0.9,
        });
        const ringSprite = new THREE.Sprite(ringMaterial);
        ringSprite.scale.setScalar(baseRadius * 0.5);
        this.mainMesh.add(ringSprite);
        this.clickShockwaves.push({
            mesh: ringSprite,
            material: ringMaterial,
            startTime: elapsed,
            duration: 0.7,
            startScale: baseRadius * 0.5,
            endScale: baseRadius * 2.2,
            startOpacity: 0.9,
            endOpacity: 0,
        });
    }

    updateClickShockwaves(elapsed) {
        if (this.clickShockwaves.length === 0) return;

        for (let i = this.clickShockwaves.length - 1; i >= 0; i--) {
            const wave = this.clickShockwaves[i];
            const t = Math.min(1, (elapsed - wave.startTime) / wave.duration);
            const eased = 1 - Math.pow(1 - t, 3);

            const scale = THREE.MathUtils.lerp(wave.startScale, wave.endScale, eased);
            wave.mesh.scale.setScalar(scale);
            wave.material.opacity = THREE.MathUtils.lerp(wave.startOpacity, wave.endOpacity, t);

            if (t >= 1) {
                this.mainMesh.remove(wave.mesh);
                wave.material.dispose();
                this.clickShockwaves.splice(i, 1);
            }
        }
    }

    async getMeshes() {
        await this.loadModelAsync();
        return {
            // main mesh must be named "this.mainMesh", for raycaster judging.
            mainMesh: this.mainMesh,
        };
    }

    getAnimateFunc() {
        return this.animateFunc;
    }

    getListenerFunc(listenerType) {
        if (listenerType === "click") {
            return this.whenClick;
        }
        if (listenerType === "mousemove") {
            return this.whenMouseMove;
        }
        if (listenerType === "mouseover") {
            return this.whenMouseOver;
        }
        if (listenerType === "notmouseover") {
            return this.notMouseOver;
        }
    }

    colorSet(color) {
        this.color = color;
        this.materials.forEach((material) => {
            material.color.set(color);
        });
    }

    scaleSet(x, y, z) {
        this.baseScale.set(x, y, z);
    }

    positionSet(x, y, z) {
        if (this.mainMesh) this.mainMesh.position.set(x, y, z);
    }

    rotationSet(x, y, z) {
        if (this.mainMesh) this.mainMesh.rotation.set(x, y, z);
    }
}